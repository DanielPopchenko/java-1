public class DataTypes {
    public static void main(String[] args) {
        // ! byte type can store numbers from -128 to 127
        // byte myNum = 127;
        // ! The short data type can store whole numbers from -32768 to 32767
        short myNum = 5000;
        System.out.println(myNum);
        // ! long -  to store very very big numbers you should end the value with an "L"
        long longNum = 15000000000L;
        System.out.println(longNum);
        
        // ! double - тоде самое что и float, только он позволяет писать больше знаков после запятой 
        double num = 19.92323239d;
        System.out.println(num);
    }
}
